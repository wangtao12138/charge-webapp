package cn.com.cdboost.charge.webapi.dto.info;

/**
 * 登录成功后，返回系统配置相关信息
 */
public class SystemConfigInfo {
    /**
     * 系统名称
     */
    private String sysName;

    /**
     * 背景图片路径
     */
    private String background;

    /**
     * 前置机IP
     */
    private String frontProcessorIp;

    /**
     * 前置机端口
     */
    private String frontProcessorPort;

    /**
     * APN
     */
    private String apn;

    /**
     * 结算日
     */
    private String balanceDate;

    /**
     * 支付地址
     */
    private String payAddr;

    /**
     * 公司名
     */
    private String companyName;

    /**
     * 版本号
     */
    private String version;

    /**
     * 前置机状态，1在线，0不在线
     */
    private Integer frontProcessorStatus;

    /**
     * webSocket地址
     */
    private String webSocketUrl;
    /**
     * 版权信息
     */
    private String copyright;

    public String getCopyright() {
        return copyright;
    }

    public void setCopyright(String copyright) {
        this.copyright = copyright;
    }

    public String getSysName() {
        return sysName;
    }

    public void setSysName(String sysName) {
        this.sysName = sysName;
    }

    public String getBackground() {
        return background;
    }

    public void setBackground(String background) {
        this.background = background;
    }

    public String getFrontProcessorIp() {
        return frontProcessorIp;
    }

    public void setFrontProcessorIp(String frontProcessorIp) {
        this.frontProcessorIp = frontProcessorIp;
    }

    public String getFrontProcessorPort() {
        return frontProcessorPort;
    }

    public void setFrontProcessorPort(String frontProcessorPort) {
        this.frontProcessorPort = frontProcessorPort;
    }

    public String getApn() {
        return apn;
    }

    public void setApn(String apn) {
        this.apn = apn;
    }

    public String getBalanceDate() {
        return balanceDate;
    }

    public void setBalanceDate(String balanceDate) {
        this.balanceDate = balanceDate;
    }

    public String getPayAddr() {
        return payAddr;
    }

    public void setPayAddr(String payAddr) {
        this.payAddr = payAddr;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public Integer getFrontProcessorStatus() {
        return frontProcessorStatus;
    }

    public void setFrontProcessorStatus(Integer frontProcessorStatus) {
        this.frontProcessorStatus = frontProcessorStatus;
    }

    public String getWebSocketUrl() {
        return webSocketUrl;
    }

    public void setWebSocketUrl(String webSocketUrl) {
        this.webSocketUrl = webSocketUrl;
    }
}
